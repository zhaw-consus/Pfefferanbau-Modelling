

library(rvest)
library(tidyverse)

page <- read_html("Future climate, 10 minutes spatial resolution â€” WorldClim 1 documentation.html")

myurls <- page %>% 
  html_elements("a") %>% 
  html_attr("href")


data_df <- tibble(urls = myurls) %>% 
  filter(startsWith(urls, "https"),endsWith(urls, "tif"))


data_df <- data_df %>% 
  mutate(filename = basename(urls)) %>% 
  extract(filename,c("resolution","variable", "model","szenario", "time"),"(\\d+\\w+)_(tmin|tmax|prec|bioc)_(.+)_(ssp126|ssp245|ssp370|ssp585)_(\\d{4}-\\d{4})",remove = FALSE)

write_csv(data_df, "data-10min.csv")
